Object.freeze(MORSE_CODE);


function morse(text) {
  if (text === "") {
    return "";
  } if (!text === 'string') throw new Error('Please enter a string')
  return text.trim().split(' ').map(char => MORSE_CODE[char] || ' ').join('').replace(/\s+/g, ' ');
 
} 

module.exports = morse;
